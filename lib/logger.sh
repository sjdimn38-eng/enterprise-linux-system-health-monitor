#!/usr/bin/env bash

###############################################################################
# Enterprise Linux System Health Monitor (ELSHM)
# Logging Library
###############################################################################

###############################################################################
# Colors
###############################################################################

RED="\e[31m"
GREEN="\e[32m"
YELLOW="\e[33m"
BLUE="\e[34m"
CYAN="\e[36m"
RESET="\e[0m"

###############################################################################
# Log Directory
###############################################################################

PROJECT_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"

LOG_DIR="$PROJECT_ROOT/logs"

mkdir -p "$LOG_DIR"

LOG_FILE="$LOG_DIR/monitor_$(date +%F).log"

###############################################################################
# Timestamp
###############################################################################

timestamp() {

    date "+%Y-%m-%d %H:%M:%S"

}

###############################################################################
# Write Log
###############################################################################

write_log() {

    local LEVEL="${1:-INFO}"
    local MESSAGE="${2:-}"

    printf "[%s] [%s] %s\n" \
        "$(timestamp)" \
        "$LEVEL" \
        "$MESSAGE" >> "$LOG_FILE"

}

###############################################################################
# INFO
###############################################################################

log_info() {

    local MESSAGE="${1:-}"

    printf "${BLUE}[INFO]${RESET} %s\n" "$MESSAGE"

    write_log "INFO" "$MESSAGE"

}

###############################################################################
# SUCCESS
###############################################################################

log_success() {

    local MESSAGE="${1:-}"

    printf "${GREEN}[SUCCESS]${RESET} %s\n" "$MESSAGE"

    write_log "SUCCESS" "$MESSAGE"

}

###############################################################################
# WARNING
###############################################################################

log_warning() {

    local MESSAGE="${1:-}"

    printf "${YELLOW}[WARNING]${RESET} %s\n" "$MESSAGE"

    write_log "WARNING" "$MESSAGE"

}

###############################################################################
# ERROR
###############################################################################

log_error() {

    local MESSAGE="${1:-}"

    printf "${RED}[ERROR]${RESET} %s\n" "$MESSAGE"

    write_log "ERROR" "$MESSAGE"

}

###############################################################################
# HEADER
###############################################################################

log_header() {

    local MESSAGE="${1:-}"

    echo

    printf "${CYAN}============================================================${RESET}\n"
    printf "${CYAN}%s${RESET}\n" "$MESSAGE"
    printf "${CYAN}============================================================${RESET}\n"

    echo

}
